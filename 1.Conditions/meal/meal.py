def convert(time):
    #pseudo code:
    #Step1:Convert time(str) a into 24hrs format(float)
    #       Eg:like "7:30" (i.e., 7 hours and 30 minutes), convert() should return 7.5 (i.e., 7.5 hours).
    #time1 = float(time.replace(":","."))
    hours, minutes = time.split(":")
    new_mins = int(minutes)/ 60
    # print(new_mins)
    time = float(int(hours)+new_mins)
    # print(time)
    return time

